const now = new Date();
console.log(now.getFullYear());  // 2024 (current year)
console.log(now.getMonth());     // 4 (May, because months are 0-indexed)
console.log(now.getDate());      // 16 (day of the month)
console.log(now.getDay());       // 4 (Thursday, 0=Sunday)
console.log(now.getHours());     // 14 (2 PM in 24-hour format)
console.log(now.getMinutes());   // 30 (current minute)
console.log(now.getSeconds());   // 45 (current second)
console.log(now.getMilliseconds()); // 123 (current millisecond)
console.log(now.getTime());      // 1715855445123 (ms since Jan 1, 1970)




